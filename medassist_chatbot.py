def predict_disease_v5(name, age, gender, duration, existing_conditions, selected_display_symptoms):
    if not selected_display_symptoms:
        empty_msg = """<div style='text-align:center; padding:40px; color:#94A3B8;'>
        <div style='font-size:48px; margin-bottom:12px;'>🩺</div>
        <p style='font-size:1.1rem;'>Select your symptoms to get started</p>
        </div>"""
        return empty_msg, None

    selected_symptoms = [symptom_display_map[s] for s in selected_display_symptoms]

    input_vector = pd.DataFrame([np.zeros(len(symptom_list))], columns=symptom_list)
    for symptom in selected_symptoms:
        if symptom in input_vector.columns:
            input_vector[symptom] = 1

    prediction_encoded = model.predict(input_vector)[0]
    probabilities = model.predict_proba(input_vector)[0]
    confidence = probabilities[prediction_encoded] * 100
    predicted_disease = label_encoder.inverse_transform([prediction_encoded])[0]

    is_emergency = check_emergency(selected_symptoms)
    severity_label, severity_color, severity_bg = get_severity_badge(confidence, is_emergency)

    symptoms_text = ", ".join([s.replace('_', ' ') for s in selected_symptoms])
    display_name = name.strip() if name and name.strip() else "Patient"
    conditions_text = ", ".join(existing_conditions) if existing_conditions else "None reported"

    if is_emergency:
        urgency_note = "This includes symptoms that may indicate a medical emergency."
    else:
        urgency_note = "These symptoms do not appear to indicate an immediate emergency."

    conditions_note = f"The patient has the following existing medical conditions: {conditions_text}." if existing_conditions else "The patient has no reported existing medical conditions."

    prompt = f"""You are a healthcare information assistant, not a doctor.
A {age}-year-old {gender} reported these symptoms for {duration}: {symptoms_text}.
{conditions_note}
A machine learning model predicted: {predicted_disease} (confidence: {confidence:.1f}%).
{urgency_note}

Write a short, clear response (under 100 words, plain text, no markdown headers) that:
1. Briefly explains why these symptoms might relate to this condition, factoring in age, gender, symptom duration, and any existing conditions if relevant (e.g. diabetes affecting wound healing, asthma affecting breathlessness risk)
2. Gives 2-3 short practical recommendations as a simple list using a dash
3. If urgent, say to seek immediate medical attention

Use cautious language like "may suggest" or "could indicate". Do not diagnose definitively."""

    response = gemini_model.generate_content(prompt)
    explanation = response.text.strip()

    bar = confidence_bar_html(confidence)

    emergency_html = ""
    if is_emergency:
        emergency_html = """
        <div style='background:linear-gradient(135deg,#DC2626,#991B1B); color:white; padding:16px 20px;
                    border-radius:14px; margin-top:16px; font-weight:600;'>
            🚨 Seek Immediate Medical Attention — some selected symptoms may indicate a medical emergency.
        </div>"""

    result_html = f"""
    <div style='background:white; border-radius:20px; padding:0; overflow:hidden;
                box-shadow:0 8px 30px rgba(15,118,110,0.10); border:1px solid #E2E8F0;'>

        <div style='background:linear-gradient(135deg,#0F766E,#14B8A6); padding:20px 24px; color:white;'>
            <div style='font-size:0.85rem; opacity:0.9; margin-bottom:4px;'>POSSIBLE CONDITION</div>
            <div style='font-size:1.5rem; font-weight:700;'>{predicted_disease}</div>
            <div style='font-size:0.8rem; opacity:0.85; margin-top:6px;'>{display_name} • {age} years, {gender} • Duration: {duration}</div>
        </div>

        <div style='padding:20px 24px;'>
            <div style='display:flex; justify-content:space-between; align-items:center; margin-bottom:16px;'>
                <div>
                    <div style='font-size:0.8rem; color:#64748B; margin-bottom:4px;'>CONFIDENCE</div>
                    <div style='font-family:monospace; font-size:1.1rem; color:#0F766E; letter-spacing:1px;'>{bar} {confidence:.0f}%</div>
                </div>
                <div style='background:{severity_bg}; color:{severity_color}; padding:8px 16px;
                            border-radius:999px; font-weight:600; font-size:0.9rem; white-space:nowrap;'>
                    {severity_label}
                </div>
            </div>

            <div style='height:1px; background:#E2E8F0; margin:16px 0;'></div>

            <div style='color:#334155; line-height:1.7; font-size:0.97rem;'>
                {explanation}
            </div>

            {emergency_html}

            <div style='margin-top:18px; padding:12px 16px; background:#F8FAFC; border-radius:10px;
                        font-size:0.82rem; color:#64748B; border-left:3px solid #14B8A6;'>
                ⚠️ AI-generated information tool, not a medical diagnosis. Consult a licensed healthcare professional.
            </div>
        </div>
    </div>
    """

    safe_name = "".join(c for c in display_name if c.isalnum()) or "Patient"
    pdf_filename = f"MedAssist_Report_{safe_name}_{datetime.now().strftime('%Y%m%d_%H%M%S')}.pdf"
    build_pdf_report(display_name, age, gender, duration, conditions_text, symptoms_text,
                      predicted_disease, confidence, severity_label, explanation, pdf_filename)

    return result_html, pdf_filename

