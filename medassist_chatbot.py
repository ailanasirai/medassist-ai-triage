"""## **Step 2: Train Multi-Class Disease Prediction Model**
132 binary symptoms as features, predicting one of 41 diseases.

## Prepare features
"""

from sklearn.ensemble import RandomForestClassifier
from sklearn.model_selection import cross_val_score
from sklearn.preprocessing import LabelEncoder

# Remove junk unnamed column if present (CSV formatting artifact)
if 'Unnamed: 133' in train_data.columns:
    train_data = train_data.drop('Unnamed: 133', axis=1)

X = train_data.drop('prognosis', axis=1)
y = train_data['prognosis']

label_encoder = LabelEncoder()
y_encoded = label_encoder.fit_transform(y)

print("Number of features:", X.shape[1])
print("Number of classes:", len(label_encoder.classes_))

"""## Train and cross Validate"""

model = RandomForestClassifier(n_estimators=200, random_state=42)

cv_scores = cross_val_score(model, X, y_encoded, cv=5)
print("Cross-Validation Accuracy:", cv_scores.mean())

model.fit(X, y_encoded)
print("Model trained on full dataset.")

"""## Test on Held-Out Test Set"""

X_test = test_data.drop('prognosis', axis=1)
y_test = test_data['prognosis']
y_test_encoded = label_encoder.transform(y_test)

test_preds = model.predict(X_test)
test_accuracy = (test_preds == y_test_encoded).mean()

print("Test Set Accuracy:", test_accuracy)

