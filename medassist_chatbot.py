EMERGENCY_SYMPTOMS = [
    'chest_pain', 'breathlessness', 'coma', 'blood_in_sputum',
    'bleeding', 'unconsciousness'
]

def check_emergency(selected_symptoms):
    """Check if any selected symptom is a red-flag emergency symptom."""
    for symptom in selected_symptoms:
        if symptom in EMERGENCY_SYMPTOMS:
            return True
    return False

