def update_conditions_choices(gender):
    if gender == "Female":
        return gr.update(choices=["Diabetes", "Hypertension", "Asthma", "Heart Disease", "Kidney Disease", "Pregnancy"])
    else:
        return gr.update(choices=["Diabetes", "Hypertension", "Asthma", "Heart Disease", "Kidney Disease"], value=[])


def filter_symptoms(search_text, currently_selected):
    if not search_text:
        return gr.update(choices=symptom_display_choices, value=currently_selected)

    search_lower = search_text.lower()
    filtered = [s for s in symptom_display_choices if search_lower in s.lower()]

    # Keep already-selected symptoms visible even if they don't match the search,
    # so the user doesn't lose their previous selections
    for s in currently_selected:
        if s not in filtered:
            filtered.append(s)

    return gr.update(choices=filtered, value=currently_selected)


